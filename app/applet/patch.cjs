const dtPath = path.join(__dirname, 'src', 'data', 'ilju-dataset.ts');
let ds = fs.readFileSync(dtPath, 'utf8');

for (const [key, mods] of Object.entries(updates)) {
  const modStr = `timing_modifiers: {
      wood: '${mods.wood}',
      fire: '${mods.fire}',
      earth: '${mods.earth}',
      metal: '${mods.metal}',
      water: '${mods.water}'
    }`;
    
  // Locate the specific ilju block
  const iljuRegex = new RegExp(`('${key}'|"${key}"): \\{[\\s\\S]*?narrative_blocks: \\{[\\s\\S]*?\\}`);
  
  ds = ds.replace(iljuRegex, (match) => {
    // Check if timing_modifiers already exist, if so replace, if not add
    if (match.includes('timing_modifiers:')) {
       return match.replace(/timing_modifiers: \{[\s\S]*?\}/, modStr);
    } else {
       // Insert it after relationship:
       return match.replace(/(relationship: '[^']+')(,|\n)/, `$1,\n      ${modStr}$2`);
    }
  });
}

fs.writeFileSync(dtPath, ds, 'utf8');
